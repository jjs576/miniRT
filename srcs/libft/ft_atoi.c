#include "libft.h"

int		ft_atoi(char *line, int *nb)
{
	int		i;
	long	nnb;

	i = 0;
	nnb = 0;
	if (line[i] == '-')
		i++;
	while (line[i] && line[i] >= '0' && line[i] <= '9')
		nnb = 10 * nnb + (line[i++] - '0');
	if (i == 0 || (line[0] == '-' && i == 1))
		return (-1);
	*nb = (line[0] == '-') ? -nnb : nnb;
	return (i);
}
